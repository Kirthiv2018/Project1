package workoutprograms;

public class ArrayShortcut {

	public static void main(String[] args) {

		
		int[] anArray = { 
			    100, 200, 300,
			    400, 500, 600, 
			    700, 800, 900, 1000
			};
		
		for (int i=0; i<anArray.length; i++) {
			
			System.out.println(anArray[i]);
			
		}
		
		
		
	}

}
